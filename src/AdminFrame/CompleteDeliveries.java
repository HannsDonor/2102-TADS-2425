    private void btnCompleteDeliveryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompleteDeliveryActionPerformed
        int SelectedTruckID = Integer.parseInt(edtTruckID.getText());
        
        Reusable r = new Reusable();
        if(!r.checkTruckID(SelectedTruckID)){
            JOptionPane.showMessageDialog(null, "Truck ID not found");
            return;
        }
        
        r.UpdateTruckStatus(SelectedTruckID, "Available");
        r.updateDeliveryStatus(SelectedTruckID, "Completed");
        r.updatePackageStatus(SelectedTruckID, "Delivered");
        
        SelectedTruckID = 0;
        
        AdminFrame AD = new AdminFrame();
        AD.viewDeliveries();
        
        
    }//GEN-LAST:event_btnCompleteDeliveryActionPerformed

