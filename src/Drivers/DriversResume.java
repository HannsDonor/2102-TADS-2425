    public void trackUserID(String Username){
        try{
          Connection conn = DriverManager.getConnection(url, user, pass);
          String sql = "SELECT * FROM Users WHERE Username = ?";
          PreparedStatement pstmt = conn.prepareStatement(sql);
          pstmt.setString(1, Username);
          ResultSet rs = pstmt.executeQuery();
          
          if(rs.next()){
              int UserID = rs.getInt("UserID");
              DriversSessionManager.getInstance().setUserID(UserID);
          }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void btnCreateDriverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateDriverActionPerformed
        int Age = Integer.parseInt(edtAge.getText());
        int Experience = Integer.parseInt(edtExperience.getText());
        String Address = edtAddress.getText();
        
        String Gender = DriversSessionManager.getInstance().getGender();
        if (Gender == null || Gender.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please pick a gender");
        return;
        }
        
        Create_Account CA = new Create_Account();
        String Username = DriversSessionManager.getInstance().getUsername();
        String Password = DriversSessionManager.getInstance().getPassword();
        CA.createAccount(Username, Password, "Driver");
        
        trackUserID(Username);
        
        int UserID = DriversSessionManager.getInstance().getUserID();
        driversInfo(Username, UserID, Age, Experience, Address, Gender);
    }//GEN-LAST:event_btnCreateDriverActionPerformed

