    public void driversInfo(String Username, int UserID, int Age, int Experience, String Address, String Gender){
        try{
            Connection conn = DriverManager.getConnection(url, user, pass);
            String sql = "INSERT INTO DriversInfo (DriverName, UserID, Age, Experience, Address, Gender) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setString(1, Username);
            pstmt.setInt(2, UserID);
            pstmt.setInt(3, Age);
            pstmt.setInt(4, Experience);
            pstmt.setString(5, Address);
            pstmt.setString(6, Gender);
            int rowsAffected = pstmt.executeUpdate();
            
            if(rowsAffected > 0){
                JOptionPane.showMessageDialog(null, "Driver Account Created");
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
